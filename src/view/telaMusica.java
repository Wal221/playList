    private void ordenaMusicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ordenaMusicActionPerformed

        music.ordDuracao();
         limpar();
        mostrar();
                 
    }//GEN-LAST:event_ordenaMusicActionPerformed

    
    public void limpar(){
        campDuracaoMusic.setText("");
        campNomeMusic.setText("");
    }
    
    public void mostrar(){
         modelo.removeAllElements();
         for(int i = 0 ; i < music.cont; i++){
         modelo.addElement(music.mostrar(i));
         }
   
    }
