    private void adicionarMusicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarMusicActionPerformed
       String nomeMusi = campNomeMusic.getText();
       Double durMusic = Double.parseDouble(campDuracaoMusic.getText());
       
       music.add(nomeMusi, durMusic);
        limpar();
        mostrar();
    }//GEN-LAST:event_adicionarMusicActionPerformed

